fruits = ["apple", "banana", "mango", "orange"]

# Check if apple is in the list
if "apple" in fruits:
    print("apple")

# Check if banana is in the list
if "banana" in fruits:
    print("banana")

# Loop through all items in the list
for x in fruits:
    print(x)

# Loop through characters of the word "apple"
for a in "apple":
    if a == "a":
        print(a)
